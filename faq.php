				<p class="text-center" id=" hide_div">Some of our youngest students may experience some separation anxiety on the first day of class. We find the most effective method for separation anxiety is a quick but loving goodbye from the parent. Once they arrive in the classroom, students are immediately engaged in classroom activities. It is our firm policy that parents do not accompany students into the classroom. We have found that this tends to heighten the distress of the student and makes the separation even more difficult. This also sets a very difficult precedent for the other students in the class who cannot understand why their parent is not with them as well. Parents are welcome to wait in the lobby area during class. Another strategy which helps to decrease anxiety is to be sure your student arrives on time, especially for the first day of class. Late students tend to feel more anxiety and have a more difficult time joining class then those who begin class with the entire group.</p>
