				<h3 class="text-center click_me">Are actors paid?</h3>
				<p class="text-center hide_div">Adult actors are compensated on a stipend basis. Youth actors are not compensated, but gain valuable stage experience performing in a professional production alongside adult “working” actors.</p>
				<h3 class="text-center click_me">Do I need to memorize a monologue?</h3>
				<p class="text-center hide_div">Sometimes, but not always. After you sing (if the production is a musical) you may be given a copy of a scene from the play that you will be asked to read from. This is called a “cold reading.”</p>
				<h3 class="text-center click_me">What is 16 bars of sheet music?</h3>
				<p class="text-center hide_div">If you look at a piece of sheet music, you will see that the notes are on a series of 5 horizontal lines. You will also see vertical lines that separate those lines into a series of groups of notes. Each one of those vertical sections is a "bar" of music. You can choose any 16 bars for an audition, so try to pick the 16 bars that show off your voice the best. Notate on the sheet music where the pianist should start and stop. If you are still having trouble, ask someone you know who can read music to show you on the music what it is, and to help you pick 16 bars that will work for you.</p>
				<h3 class="text-center click_me">Do I need a headshot (picture) and a resume?</h3>
				<p class="text-center hide_div">When you arrive you will be given an audition form to fill out. If you have a resume we will attach it to the form. If you do not have a resume you will need to fill out the space on the audition form that asks you to list any theatrical experience. A picture is not required, but if you have a school picture, photo of yourself or a headshot, please bring it with you. It is a great way for the people who are casting the show to remember what you look like.</p>
				<h3 class="text-center click_me">Can my parents watch my audition?</h3>
				<p class="text-center hide_div">No. Parents are welcome to accompany you before and after the audition. There will be a place for them to wait while you are in your audition.</p>
				<h3 class="text-center click_me">What happens if I cannot make the audition dates?</h3>
				<p class="text-center hide_div">You may arrange a video submission.</p>
				<h3 class="text-center click_me">What should I wear?</h3>
				<p class="text-center hide_div">An audition is much like a job interview, except in this interview you might be asked to dance or sing! You should try to look professional and bring an extra set of more comfortable clothes and shoes if you know there will be a choreography portion of the audition.</p>
				<h3 class="text-center click_me">Will I have to audition in front of anyone else who is auditioning?</h3>
				<p class="text-center hide_div">Sometimes, yes. If asked to do a cold reading, you may be asked to read with other people for the scene.</p>
				<h3 class="text-center click_me">What is a callback?</h3>
				<p class="text-center hide_div">The "callback" is a second (sometimes third, fourth or fifth) audition where the selection process becomes more specific. At a callback you will be asked to read additional scenes from the show, prepare music from the score, and learn additional dance combinations. The callback is also an opportunity for the director to see and hear different combinations of actors.</p>
				<h3 class="text-center click_me">What if I am not called back?</h3>
				<p class="text-center hide_div">Not being called back doesn’t always mean that you aren’t being cast in the show. Sometimes a callback is needed for a director to look at the way specific groups of people fit together.</p>
				<h3 class="text-center click_me">What if I can’t make all rehearsals?</h3>
				<p class="text-center hide_div">A calendar with all the possible rehearsal dates and all performances is located on the back of the Audition Form. When you audition we ask that you mark any date that you can't rehearse and turn it back in with your form. We are often able to work around the availability of the actors we cast. Sometimes the availability of an actor is the final deciding factor in casting. Always be honest about your availability.</p>
				<h3 class="text-center click_me">How will I know if I am cast in the show? </h3>
				<p class="text-center hide_div">If you are cast in the show you will receive a email no later than two weeks after the audition.</p>
